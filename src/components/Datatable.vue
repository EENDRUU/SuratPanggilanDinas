<script>
import "jquery/dist/jquery.min.js";

import "datatables.net-dt/js/dataTables.dataTables";
import "datatables.net-dt/css/jquery.dataTables.min.css";
import "bootstrap/dist/css/bootstrap.min.css"
import $ from "jquery";
export default {
  mounted() {
   $('#datatable thead tr')
        .clone(true)
        .addClass('filters')
        .appendTo('#datatable thead');
 
    
    $('#datatable').DataTable({
        scrollX:true,
        scrollCollapse: true,
        fixedHeader:    true,
         fixedColumns: true,


        language: { 
          search: "",
          paginate: {
            first: $('#iconPageFirst'),
            previous: $('#iconPagePrev'),
            next: $('#iconPageNext'),
            last: $('#iconPageLast')
          }
        },
        pageLength : 5,
        lengthMenu: [[5, 10, 20, -1], [5, 10, 20, 'ALL']],
        "bInfo" : false,
        "sDom": 'Rfrtlip',
        orderCellsTop: true,
        //fixedHeader: true,
        "pagingType": "full_numbers",
        initComplete: function () {
            var api = this.api();
 
            // For each column
            api.columns().eq(0).each(function (colIdx) {
                    // Set the header cell to contain the input element
                    var cell = $('.filters th').eq(
                        $(api.column(colIdx).header()).index()
                    );
                    //var title = $(cell).text();
                    console.log(colIdx)
                    if(colIdx != 4 && colIdx !=5)
                    {
                       $(cell).html('<input type="text" placeholder="Filter " />');
                    }
                    else{
                       $(cell).html('');
                    }
                   
 
                    // On every keypress in this input
                    $('input',$('.filters th').eq($(api.column(colIdx).header()).index()))
                        .off('keyup change')
                        .on('change', function () {
                            // Get the search value
                            $(this).attr('title', $(this).val());
                            var regexr = '({search})'; //$(this).parents('th').find('select').val();
 
                            //var cursorPosition = this.selectionStart;
                            // Search the column for that value
                            api.column(colIdx).search(
                                    this.value != ''
                                        ? regexr.replace('{search}', '(((' + this.value + ')))')
                                        : '',
                                    this.value != '',
                                    this.value == ''
                                ).draw();
                        })
                        .on('keyup', function (e) {
                            var cursorPosition = this.selectionStart;
                            e.stopPropagation();
 
                            $(this).trigger('change');
                            $(this)
                                .focus()[0]
                                .setSelectionRange(cursorPosition, cursorPosition);
                        });
                });
        },
    });
    // $('#datatable_filter').hide();
   $('#datatable_filter > label > input').attr("placeholder", "search");
   $('#datatable_filter').prepend($('#btnFilter'));
  //  $('#datatable_first').prepend($('#iconPageFirst'));
  },
  data: function () {
    return {
      products: [],
    };
  },
};
